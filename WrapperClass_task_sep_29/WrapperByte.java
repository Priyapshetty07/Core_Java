class WrapperByte{

public static void main(String...args){
		
		String helmet = "1";
		byte conversionOfHelmet	=Byte.parseByte(helmet);
		System.out.println(conversionOfHelmet+2);

		String numberOfChair = "3";
		byte conversionOfChair = Byte.parseByte(numberOfChair);
		System.out.println(conversionOfChair);

		String oneDozen = "12";
		byte coversionOfDozen = Byte.parseByte(oneDozen);
		System.out.println(coversionOfDozen);
		
		String twoLitre = "2";
		byte conversionOfMilk = Byte.parseByte(twoLitre);
		System.out.println(conversionOfMilk);
		
		String noOfCylinder = "4";
		byte conversionOfCylinder = Byte.parseByte(noOfCylinder);
		System.out.println(conversionOfCylinder);
    }
}